#include "rgbapixel.h"
#include <cstdint>
using std::uint8_t;

RGBAPixel::RGBAPixel()
{
	uint8_t red = 255;
	uint8_t green = 255;
	uint8_t blue = 255;
	uint8_t alpha = 255;	
}
RGBAPixel::RGBAPixel(uint8_t r, uint8_t g,uint8_t b) {
	this -> red = r;
        this -> green = g;
	this -> blue = b;
	this -> alpha = 255;
}

